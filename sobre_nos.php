        ${cor === '#3b82f6' || cor === '#1d4ed8' || cor === '#1e40af' || cor === '#1e3a8a' || cor === '#312e81' || cor === '#581c87' || cor === '#7c2d12' ? 'color: white;' : ''}
    `;
    
    // Emoji baseado no nome do tema
    const emoji = obterEmojiArea(tema.nome);
    
    card.innerHTML = `
        <strong>${emoji} ${tema.nome}</strong>
        <div style="font-size: 0.9em; margin-top: 5px; opacity: 0.8;">
            ${tema.total_questoes} questÃµes
        </div>
    `;
    
    // Adicionar hover effect
    card.addEventListener('mouseenter', function() {
        this.style.transform = 'translateY(-2px)';
        this.style.boxShadow = '0 4px 12px rgba(0, 0, 0, 0.15)';
    });
    
    card.addEventListener('mouseleave', function() {
        this.style.transform = 'translateY(0)';
        this.style.boxShadow = 'none';
    });
    
    // Adicionar link para o sistema de questÃµes
    card.addEventListener('click', function() {
        window.location.href = 'questoes/index.php';
    });
    
    return card;
}

