async function carregarAreasCobertura() {
    try {
        // Implementar timeout de 10 segundos
        const controller = new AbortController();
        const timeoutId = setTimeout(() => controller.abort(), 10000);
        
        const response = await fetch('buscar_temas.php', {
            signal: controller.signal
        });
        clearTimeout(timeoutId);
        
        const temas = await response.json();
        
        const container = document.getElementById('areas-cobertura');
        
        if (temas && temas.length > 0) {
            // Limpar loading placeholders
            container.innerHTML = '';
            
            // Cores para os cards
            const cores = [
                '#e0f2fe', '#dbeafe', '#bfdbfe', '#93c5fd', '#60a5fa', '#3b82f6',
                '#1d4ed8', '#1e40af', '#1e3a8a', '#312e81', '#581c87', '#7c2d12'
            ];
            
            // Criar cards para cada tema
            temas.forEach((tema, index) => {
                const card = criarCardArea(tema, cores[index % cores.length]);
                container.appendChild(card);
            });
        } else {
            // Fallback: mostrar temas padrão se não houver dados
            container.innerHTML = `
                <div style="background: #e0f2fe; padding: 15px; border-radius: 8px; text-align: center;">
                    <strong>👶 Pediatria</strong>
                </div>
                <div style="background: #dbeafe; padding: 15px; border-radius: 8px; text-align: center;">
                    <strong>🧠 Neurologia</strong>
                </div>
                <div style="background: #bfdbfe; padding: 15px; border-radius: 8px; text-align: center;">
                    <strong>👴 Geriatria</strong>
                </div>
            `;
        }
    } catch (error) {
        // Fallback em caso de erro (erro silencioso para produção)
        const container = document.getElementById('areas-cobertura');
        container.innerHTML = `
            <div style="background: #f3f4f6; padding: 15px; border-radius: 8px; text-align: center;">
                <strong>📚 Temas de Terapia Ocupacional</strong>
            </div>
        `;
    }
}

function criarCardArea(tema, cor) {
    const card = document.createElement('div');
    card.style.cssText = `
        background: ${cor};
        padding: 15px;
        border-radius: 8px;
        text-align: center;
        transition: transform 0.3s ease, box-shadow 0.3s ease;
        cursor: pointer;
        ${cor === '#3b82f6' || cor === '#1d4ed8' || cor === '#1e40af' || cor === '#1e3a8a' || cor === '#312e81' || cor === '#581c87' || cor === '#7c2d12' ? 'color: white;' : ''}
    `;
    
    // Emoji baseado no nome do tema
    const emoji = obterEmojiArea(tema.nome);
    
    card.innerHTML = `
        <strong>${emoji} ${tema.nome}</strong>
        <div style="font-size: 0.9em; margin-top: 5px; opacity: 0.8;">
            ${tema.total_questoes} questões
        </div>
    `;
    
    // Adicionar hover effect
    card.addEventListener('mouseenter', function() {
        this.style.transform = 'translateY(-2px)';
        this.style.boxShadow = '0 4px 12px rgba(0, 0, 0, 0.15)';
    });
    
    card.addEventListener('mouseleave', function() {
        this.style.transform = 'translateY(0)';
        this.style.boxShadow = 'none';
    });
    
    // Adicionar link para o sistema de questões
    card.addEventListener('click', function() {
        window.location.href = 'questoes/index.php';
    });
    
    return card;
}

function obterEmojiArea(nome) {
    const nomeLower = nome.toLowerCase();
    
    if (nomeLower.includes('desenvolvimento') || nomeLower.includes('infantil') || nomeLower.includes('criança')) {
        return '👶';
    } else if (nomeLower.includes('neurologia') || nomeLower.includes('neurológico') || nomeLower.includes('avc')) {
        return '🧠';
    } else if (nomeLower.includes('geriatria') || nomeLower.includes('idoso') || nomeLower.includes('envelhecimento')) {
        return '👴';
    } else if (nomeLower.includes('mental') || nomeLower.includes('psiquiatria') || nomeLower.includes('psicológico')) {
        return '🧘';
    } else if (nomeLower.includes('físico') || nomeLower.includes('fisioterapia') || nomeLower.includes('reabilitação')) {
        return '🏥';
    } else if (nomeLower.includes('escola') || nomeLower.includes('educação') || nomeLower.includes('aprendizagem')) {
        return '🎓';
    } else if (nomeLower.includes('trabalho') || nomeLower.includes('profissional') || nomeLower.includes('ocupacional')) {
        return '💼';
    } else if (nomeLower.includes('concurso') || nomeLower.includes('prova') || nomeLower.includes('edital')) {
        return '📝';
    } else {
        return '📚';
    }
}
